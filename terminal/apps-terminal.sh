# I don't use all of these (yet), but they look useful or cool, or both.
#
# fzf = A general-purpose command-line fuzzy finder. https://github.com/junegunn/fzf  
# ripgrep = A (much) faster grep. https://github.com/BurntSushi/ripgrep
# bat = cat with wings, see: https://github.com/sharkdp/bat
# eza = A modern, maintained replacement for ls, written in rust. https://eza.rocks/
# zoxide = A smarter cd command. https://github.com/ajeetdsouza/zoxide 
# plocate = A faster locate. https://plocate.sesse.net/ 
# btop = Resource monitor that shows usage and stats for processor, memory, disks, network and processes. https://github.com/aristocratos/btop
# apache2-utils = Not certain, but this SO answer may explain why this is here: https://askubuntu.com/a/253596
# fd-find = fd is a program to find entries in your filesystem. It is a simple, fast and user-friendly alternative to find. https://github.com/sharkdp/fd
# tldr = A community effort to simplify the beloved man pages with practical examples. https://tldr.sh/
